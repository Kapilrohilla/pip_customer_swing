                    jLabel37.setText(trade);
                    jLabel38.setText(gtc);
                    jLabel39.setText(filing);
                    jLabel40.setText(order);
                    jLabel41.setText(expiration);
                    jLabel42.setText(swapType);
                    jLabel43.setText(swapShort);
                    jLabel44.setText(swapLong);

                    System.out.println("workng5");
