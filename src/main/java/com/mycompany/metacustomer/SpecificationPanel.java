                    String symbol = rJSON.getString("symbol");
                    String desc = rJSON.getString("description");
                    String digit = rJSON.getString("digits");
                    String minVol = rJSON.getDouble("minVolume") + "";
                    String maxVol = rJSON.getDouble("maxVolume") + "";
                    String stepVol = rJSON.getString("stepVolume");
                    String cSize = rJSON.getString("contractSize");
                    String spread = rJSON.getString("spread");
                    String source = rJSON.getString("source");
                    String stopsLevel = rJSON.getString("stopsLevel");
                    String marginCurrency = rJSON.getString("marginCurrency");
                    String pCurrency = rJSON.getString("profitCurrency");
                    String calc = rJSON.getString("calculation");
                    String chartMode = rJSON.getString("chartMode");
                    String trade = rJSON.getString("trade");
                    String gtc = rJSON.getString("gtc");
                    String filing = rJSON.getString("fillings");
                    String order = rJSON.getString("order");
                    String expiration = rJSON.getString("expiration");
                    String swapType = rJSON.getString("swapType");
                    String swapShort = rJSON.getString("swapShort");
                    String swapLong = rJSON.getString("swapLong");

                    
                    jLabel23.setText(symbol);
                    jLabel24.setText(desc);
                    jLabel25.setText(digit);
                    jLabel26.setText(minVol);
                    jLabel27.setText(maxVol);
                    jLabel28.setText(stepVol);
                    jLabel29.setText(cSize);
                    jLabel30.setText(spread);
                    jLabel31.setText(source);
